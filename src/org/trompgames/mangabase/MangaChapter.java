package org.trompgames.mangabase;

import java.util.ArrayList;

public class MangaChapter {

	private String name;
	private int pages;	
	
	
	public MangaChapter(String name, int pages){
		this.name = name;
		this.pages = pages;
	}
	
	public String getName() {
		return name;
	}

	public int getPages() {
		return pages;
	}

	
	
	
}
